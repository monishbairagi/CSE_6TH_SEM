-- Creating Department Table
CREATE TABLE DEP (
	DID INT(3) NOT NULL,
	DNAME VARCHAR(20) NOT NULL,
	PRIMARY KEY(DID)
);

-- Creating Student Table
CREATE TABLE STU (
	SID INT(5) NOT NULL,
	SNAME VARCHAR(20) NOT NULL,
	MARKS DOUBLE(5,2),
	DID INT(3) NOT NULL,
	PRIMARY KEY(SID),
	FOREIGN KEY(DID) REFERENCES DEP(DID)
);

-- Creating Extra Curriculam Activity Table
CREATE TABLE ECD (
	EID INT(3) NOT NULL,
	ENAME VARCHAR(10),
	PRIMARY KEY(EID)
);

-- Creating Students Participation Table
CREATE TABLE ECP (
	SID INT(5) NOT NULL,
	EID INT(3) NOT NULL,
	FOREIGN KEY(SID) REFERENCES STU(SID),
	FOREIGN KEY(EID) REFERENCES ECD(EID)
);

